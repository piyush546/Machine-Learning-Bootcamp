# -*- coding: utf-8 -*-

""" A program to check functionality of replace method of str datatype """

# to Take input String from the user
Str = input()

# To replace the whitespaces in the string with *
Str = Str.replace(" ", "*")

# To print the output
print(Str)

# Input - Welcome to Pink City Jaipur!
# Ouput - Welcome*to*Pink*City*Jaipur!
